function draw() {
	resizeCanvas(windowWidth, windowHeight);
	x = width;
	y = height;
	opacity = 0.255*x;
	rgb = 0.255*y;
	bgr = 0.255*x;
	background(rgb,100,3/2*rgb);
	noStroke();
	//Assignment 3a: Control the position of two rectangles with one variable.
	push();
		translate(0,(1/2*y)+45);
		rotate(radians(25));
		fill(255,255,255,30);
		rect(0,0,1/2*y,y);
	pop();
	push();
		fill(255,255,255,30);
		rect(0,0,2/3*x,20*x);
	pop();
	//Assignment 3b: Control the position and size of two rectangles with two variables.
	push();
		fill(255,255,255,2*opacity);
		rect(0,2/3*y,1/3*x,10);
		rect(0,2.4/3*y,2/3*x,15);
	pop();
	push();
		fill(bgr,bgr,bgr,0.2*opacity);
		rect(0,2/3*y,1/3*x,5);
		rect(0,2.4/3*y,2/3*x,7.5);
	pop();
	push();
		fill(255,255,rgb);
		translate((1/3*x)+8,(2/3*y)+8);
		rotate(radians(-30));
		rect(0,0,3*y,2);
		rotate(radians(-30));
		rect(0,0,3*y,2);
		rotate(radians(-10));
		rect(0,0,3*y,2);
		rotate(radians(-30));
		rect(0,0,3*y,2);
	pop();
	push();
		fill(255,255,rgb,30);
		translate((1/3*x)+8,(2/3*y)+8);
		triangle(0,0,2.598*y,-1.5*y,-1.928*y,-2.298*y);
		triangle(0,0,2.598*y,-1.5*y,-0.5209*y,-2.9544*y);
	pop();
	//Assignment 3d: Create a composition that scales proportionally with different canvas sizes.
	push();
		fill(255,255,255,2*opacity);
		rect(1/3*x,0,10,(2/3*y)+10);
		rect(2/3*x,0,15,(2.4/3*y)+15);
	pop();
	push();
		fill(bgr,bgr,bgr,0.2*opacity);
		rect(1/3*x,0,5,(2/3*y)+10);
		rect(2/3*x,0,7.5,(2.4/3*y)+15);
	pop();
	//Assignment 3c: Control the properties (color, alpha, stroke, size, position,etc) of a line and an ellipse with two variables.
	push();
		fill(255,255,255,opacity);
		ellipse(1/10*x,1/10*y,1/10*x,1/10*x);
		strokeWeight(3);
		stroke(100,rgb,rgb,opacity*2);
		line(0,y-10,1/2*x,y-10);
		line(0,y-20,1/2*x,y-20);
		line(0,y-30,1/2*x,y-30);
	pop();
	push();
		fill(255,255,255,100-rgb);
		ellipse(2/10*x,3/20*y,1/11*x,1/11*x);
		ellipse(3/10*x,4/20*y,1/12*x,1/12*x);
		ellipse(4/10*x,5/20*y,1/13*x,1/13*x);
		ellipse(5/10*x,6/20*y,1/14*x,1/14*x);
		ellipse(6/10*x,7/20*y,1/15*x,1/15*x);
		ellipse(7/10*x,8/20*y,1/16*x,1/16*x);
		ellipse(8/10*x,9/20*y,1/17*x,1/17*x);
		ellipse(9/10*x,10/20*y,1/18*x,1/18*x);
		ellipse(x,11/20*y,1/19*x,1/19*x);
	pop();
	//Assignment 3e: Use multiplication to create a series of lines with increasing space between each.
	push();
		strokeWeight(3);
		stroke(rgb,rgb,100,opacity*2);
		evar = 15;
		line(0,y-(evar*3),1/2*x,y-(evar*3));
		line(0,y-(evar*4),1/2*x,y-(evar*4));
		line(0,y-(evar*5),1/2*x,y-(evar*5));
	pop();
	push();
		strokeWeight(3);
		stroke(rgb,100,rgb,opacity*2);
		evar = 20;
		line(0,y-(evar*5),1/2*x,y-(evar*5));
		line(0,y-(evar*6),1/2*x,y-(evar*6));
		line(0,y-(evar*7),1/2*x,y-(evar*7));
	pop();
	push();
		strokeWeight(3);
		stroke(rgb,150,rgb,opacity*2);
		evar = 30;
		line(0,y-(evar*6),1/2*x,y-(evar*6));
		line(0,y-(evar*7),1/2*x,y-(evar*7));
		line(0,y-(evar*8),1/2*x,y-(evar*8));
	pop();
	push();
		strokeWeight(3);
		stroke(50,rgb,rgb,opacity*2);
		evar = 30;
		line(0,y-(evar*6),1/2*x,y-(evar*6));
		line(0,y-(evar*7),1/2*x,y-(evar*7));
		line(0,y-(evar*8),1/2*x,y-(evar*8));
	pop();
	push();
		strokeWeight(3);
		stroke(rgb,rgb,50,opacity*2);
		evar = 40;
		line(0,y-(evar*6),1/2*x,y-(evar*6));
		line(0,y-(evar*7),1/2*x,y-(evar*7));
		line(0,y-(evar*8),1/2*x,y-(evar*8));
	pop();
	//Assignment 3f: Rotate 3 or more squares with different angles and put them in a graphically meaningful relationship.
	push();
		fill(255,255,255);
		translate(1/6*x,5/6*y);
		rotate(radians(35));
		rect(0,0,1/5*y,1/5*y);
		fill(198, 252, 3,2*opacity);
		rect(0,0,1/5*y,1/5*y);
		fill(0,150,255,255-bgr);
		rotate(radians(15));
		ellipse(1/20*y,1/20*y,1/10*y,1/10*y);
	pop();
	push();
		fill(255,255,255);
		translate(1/3*x,5/7*y);
		rotate(radians(70));
		rect(0,0,1/5*y,1/5*y);
		fill(198, 252, 3,2*opacity);
		rect(0,0,1/5*y,1/5*y);
		fill(0,150,255,255-bgr);
		rotate(radians(15));
		ellipse(1/20*y,1/20*y,1/5*y,1/5*y);
	pop();
	push();
		fill(255,255,255);
		translate(1/2*x,5/8*y);
		rotate(radians(105));
		rect(0,0,1/5*y,1/5*y);
		fill(198, 252, 3,2*opacity);
		rect(0,0,1/5*y,1/5*y);
		fill(0,150,255,255-bgr);
		rotate(radians(15));
		ellipse(1/20*y,1/20*y,1/3*y,1/3*y);
	pop();
}